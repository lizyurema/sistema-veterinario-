    private void grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_grabarActionPerformed
        Gestor g=new Gestor();
        g.grabar(t1.getText(),t2.getText(), t3.getText(), t4.getText(), t5.getText());
        HashMap x=g.verLineas();
        Iterator i=new TreeMap(x).keySet().iterator();
        while(i.hasNext()){
            String dni=(String)i.next();
            Cliente cli=(Cliente)x.get(dni);
            String[]dat={cli.getDni(),cli.getNom(),cli.getSex(),cli.getEst(),String.valueOf(cli.getLin())};
            modelo.addRow(dat);
            
        }
    }//GEN-LAST:event_grabarActionPerformed

